    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response); //default
        
        // Get the comment content from the form...
        String content = request.getParameter("content");
        
        // Open file for writing
        File file = new File(getServletContext().getRealPath("/") + "comments.txt");
        if(!file.exists()) { file.createNewFile(); } //create file if it doesnt exist
        BufferedWriter fout = new BufferedWriter(new FileWriter(file, true));
        
        // Write the new comment at the end of the file...
        fout.newLine();
        fout.write(content); //ele says we might need a "/n"
        fout.close();
        
        response.sendRedirect("LoadComments");
    }

