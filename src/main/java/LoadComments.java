    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        
        // store list of comments
        List<Comment> comments = new ArrayList<Comment>();
        
        // Open file for reading...
        File file = new File(getServletContext().getRealPath("/") + "comments.txt");
        if(!file.exists()) { file.createNewFile(); } //create a file if one doesn't exist
        BufferedReader reader = new BufferedReader(new FileReader(file));
        
        // Pull comments from the file...
        String text;
        while((text = reader.readLine()) != null) { comments.add(new Comment(text)); }
        Collections.reverse(comments);
        request.setAttribute("comments", comments);
        request.getRequestDispatcher("showComments.jsp").forward(request, response);
    }

